print("\n Bienvenido al cálculo de operaciones artiméticas")

num_1 = int(input("\nIntroduce un primer número:  \n"))
num_2 = int(input("\nIntroduce un segundo número:  \n"))

print(f"\n La suma de {num_1} más {num_2} es igual a {num_1 + num_2}\n") 
print(f"\n La resta de {num_1} menos {num_2} es igual a {num_1 - num_2}\n")
print(f"\n El producto de {num_1} por {num_2} es igual a {num_1 * num_2}\n")
print(f"\n La division de {num_1} entre {num_2} es igual a {num_1 / num_2}\n")
print(f"\n El cociente de la división de {num_1} entre {num_2} es igual a {num_1 // num_2}\n")
print(f"\n El resto de la división de {num_1} entre {num_2} es igual a {num_1 % num_2}\n")
